package org.pageObjects.android;

import io.appium.java_client.android.AndroidDriver;
import io.appium.java_client.pagefactory.AndroidFindBy;
import io.appium.java_client.pagefactory.AppiumFieldDecorator;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.PageFactory;
import org.utils.AndroidActions;

public class AccessibilityPage extends AndroidActions {

    AndroidDriver driver;

    public AccessibilityPage(AndroidDriver driver) {
        super(driver);
        this.driver = driver;
        PageFactory.initElements(new AppiumFieldDecorator(driver), this);
    }

    @AndroidFindBy(accessibility = "Accessibility Node Provider")
    private WebElement accessibilityNode;

    public String getTextForAccessibility() {
        return accessibilityNode.getAttribute("text");
    }
}
